def build_positive_framing_block(rules: dict = None) -> str:
    """将正向行为映射规则组装为 Prompt 片段

    Args:
        rules: 规则字典，默认使用 POSITIVE_FRAMING_MAP

    Returns:
        可注入 Prompt 的行为协议文本块
    """
    if rules is None:
        rules = POSITIVE_FRAMING_MAP

    parts = ["【行为协议 — 当你遇到以下场景时，必须执行对应动作】\n"]
    for rule_name, rule in rules.items():
        parts.append(f"场景：{rule['condition']}")
        parts.append(f"动作：{rule['action']}")
        # 只注入1个示例（节省 Token），正反各一行
        if rule.get('examples') and len(rule['examples']) >= 2:
            parts.append(f"  {rule['examples'][0]}")
            parts.append(f"  {rule['examples'][1]}")
        parts.append("")
    return "\n".join(parts)
